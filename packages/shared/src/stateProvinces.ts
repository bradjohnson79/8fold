export type StateProvinceAbbr = keyof typeof stateProvinceMap;

export function formatStateProvince(abbrRaw: string | null | undefined): string {
  const abbr = String(abbrRaw ?? "").trim().toUpperCase();
  if (!abbr) return "—";
  const full = (stateProvinceMap as any)[abbr] as string | undefined;
  return full ? `${abbr} – ${full}` : abbr;
}

